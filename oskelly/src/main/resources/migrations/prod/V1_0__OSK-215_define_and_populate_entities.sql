CREATE TABLE brand
(
  id   BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL
);

CREATE TABLE category
(
  id            BIGSERIAL PRIMARY KEY,
  url_name      TEXT    NOT NULL,
  display_name  TEXT    NOT NULL,
  left_order    INTEGER NOT NULL,
  right_order   INTEGER NOT NULL,
  parent_id     BIGINT  NOT NULL REFERENCES category (id),
  singular_name TEXT
);

CREATE TABLE size
(
  id            BIGSERIAL PRIMARY KEY,
  category_id   BIGINT NOT NULL REFERENCES category (id),
  russian       TEXT,
  european      TEXT,
  american      TEXT,
  international TEXT
);

CREATE TABLE attribute
(
  id   BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL UNIQUE
);

CREATE TABLE category_attribute_binding
(
  id           BIGSERIAL PRIMARY KEY,
  category_id  BIGINT REFERENCES category (id),
  attribute_id BIGINT REFERENCES attribute (id),
  UNIQUE (category_id, attribute_id)
);

CREATE TABLE attribute_value
(
  id           BIGSERIAL PRIMARY KEY,
  attribute_id BIGINT NOT NULL REFERENCES attribute (id),
  value        TEXT,
  UNIQUE (attribute_id, value)
);

CREATE TABLE product_condition
(
  id   BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  description TEXT
);

CREATE TABLE product
(
  id                   BIGSERIAL PRIMARY KEY,
  name                 TEXT,

  brand_id             BIGINT NOT NULL REFERENCES brand (id),
  category_id          BIGINT NOT NULL REFERENCES category (id),
  seller_id            BIGINT NOT NULL REFERENCES "user" (id),
  product_condition_id BIGINT REFERENCES product_condition (id),

  product_state        TEXT   NOT NULL,
  description          TEXT,
  payment_description  TEXT,
  delivery_description TEXT,
  publish_time         TIMESTAMP WITH TIME ZONE,
  vintage BOOLEAN NOT NULL DEFAULT FALSE,

  origin TEXT,
  purchase_price NUMERIC,
  purchase_year INTEGER,

  size_type TEXT,
  our_choice_status_time TIMESTAMPTZ
);

CREATE TABLE product_item (
	id BIGSERIAL PRIMARY KEY,
	product_id BIGINT REFERENCES product(id) NOT NULL,
	size_id BIGINT REFERENCES size (id),
	start_price NUMERIC,
	current_price NUMERIC,
	buy_price NUMERIC,
	serial_number TEXT,
	buyer_id BIGINT REFERENCES "user" (id),
	reserve_expire_time TIMESTAMPTZ NULL,
	reserver_id BIGINT NULL REFERENCES "user"(id),
	reserve_type TEXT NULL,
  model TEXT
);

CREATE TABLE product_attribute_value_binding
(
  id                 BIGSERIAL PRIMARY KEY,
  product_id         BIGINT REFERENCES product (id),
  attribute_value_id BIGINT REFERENCES attribute_value (id),
  UNIQUE (product_id, attribute_value_id)
);

CREATE TABLE product_status
(
  id   BIGSERIAL PRIMARY KEY,
  name TEXT NOT NULL
);

CREATE TABLE product_status_binding
(
  id                BIGSERIAL PRIMARY KEY,
  product_id        BIGINT REFERENCES product (id),
  product_status_id BIGINT REFERENCES product_status (id),
  UNIQUE (product_id, product_status_id)
);

CREATE TABLE image
(
  id             BIGSERIAL PRIMARY KEY,
  product_id     BIGINT REFERENCES product (id),
  image_path     TEXT                  NOT NULL,
  photo_order INTEGER NOT NULL DEFAULT 1,
  is_main        BOOLEAN DEFAULT FALSE NOT NULL
);

CREATE TABLE wish_list
(
  id         BIGSERIAL PRIMARY KEY,
  user_id    BIGINT REFERENCES "user" (id),
  product_item_id BIGINT REFERENCES product_item(id),
  UNIQUE (product_item_id, user_id)
);

CREATE TABLE authority
(
  id   BIGSERIAL PRIMARY KEY,
  name TEXT
);

