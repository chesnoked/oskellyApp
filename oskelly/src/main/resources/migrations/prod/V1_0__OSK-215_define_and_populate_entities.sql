CREATE TABLE shopping_cart
(
  id         BIGSERIAL PRIMARY KEY,
  user_id    BIGSERIAL                              NOT NULL REFERENCES "user" (id),
  add_time   TIMESTAMP WITH TIME ZONE DEFAULT now() NOT NULL,
